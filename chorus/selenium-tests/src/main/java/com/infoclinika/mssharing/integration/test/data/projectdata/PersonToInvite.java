package com.infoclinika.mssharing.integration.test.data.projectdata;

/**
 * @author Alexander Orlov
 */
public class PersonToInvite {

    private final String name;
    private final String email;

    private PersonToInvite(Builder builder) {
        this.name = builder.name;
        this.email = builder.email;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public static class Builder {
        private String name;
        private String email;

        public Builder name(String name) {
            this.name = name;
            return this;
        }

        public Builder email(String email) {
            this.email = email;
            return this;
        }

        public PersonToInvite build() {
            return new PersonToInvite(this);
        }
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        PersonToInvite that = (PersonToInvite) o;

        if (email != null ? !email.equals(that.email) : that.email != null) return false;
        if (name != null ? !name.equals(that.name) : that.name != null) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = name != null ? name.hashCode() : 0;
        result = 31 * result + (email != null ? email.hashCode() : 0);
        return result;
    }

    @Override
    public String toString() {
        return "PersonToInvite{" +
                "name='" + name + '\'' +
                ", email='" + email + '\'' +
                '}';
    }
}
